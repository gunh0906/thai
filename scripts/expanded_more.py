from __future__ import annotations


def pack_entries(tags: list[str], rows: list[tuple], note: str = "") -> list[dict]:
    items: list[dict] = []
    for row in rows:
        thai, korean, *extra = row
        thai_script = extra[0] if len(extra) >= 1 else ""
        keywords = extra[1] if len(extra) >= 2 else None
        item = {
            "thai": thai,
            "korean": korean,
            "tags": tags,
            "note": note,
        }
        if thai_script:
            item["thaiScript"] = thai_script
        if keywords:
            item["keywords"] = keywords
        items.append(item)
    return items


